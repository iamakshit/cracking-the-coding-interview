package com.java.tech.interview.mobikwik;

public class MWClass {

	public String a;
	public Integer b;
	public String c;

	public String getA() {
		return a;
	}

	public void setA(String a) {
		this.a = a;
	}

	public Integer getB() {
		return b;
	}

	public void setB(Integer b) {
		this.b = b;
	}

	public String getC() {
		return c;
	}

	public void setC(String c) {
		this.c = c;
	}

	@Override
	public String toString() {
		return "MWClass [a=" + a + ", b=" + b + ", c=" + c + "]";
	}

}
